    required this.monthlySavings,
    required this.extraSavings,
    required this.loanRepaid,
    required this.serviceCharge,
    required this.penalty,
    required this.otherDeposits,
    required this.totalDeposit,
    required this.givenLoan,
    required this.loanInterest,
    required this.totalLoanPaid,
    required this.totalSavings,
    required this.totalExtraSavings,
    required this.savingsBack,
    required this.loanTakenTillDate,
    required this.totalLoanReturn,
    required this.remainingLoan,
    required this.totalServiceCharge,
    required this.totalPenalty,
    required this.totalOtherSavings,
  });
}
