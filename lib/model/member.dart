class Member {
  final String name;
  final String monthlySavings;
  final String extraSavings;
  final String loanRepaid;
  final String serviceCharge;
  final String penalty;
  final String otherDeposits;
  final String totalDeposit;
  final String givenLoan;
  final String loanInterest;
  final String totalLoanPaid;
  final String totalSavings;
  final String totalExtraSavings;
  final String savingsBack;
  final String loanTakenTillDate;
  final String totalLoanReturn;
  final String remainingLoan;
  final String totalServiceCharge;
  final String totalPenalty;
  final String totalOtherSavings;

  Member({
